    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed

    if (listaVuelos.getSelectedIndex() >= 0) {
        
        DatosReserva.limpiar();
      
        try {
            DatosReserva.totalPasajeros = (int) espPasajeros.getValue();
        } catch (Exception e) {
      
             DatosReserva.totalPasajeros = 1;
        }
        
        String texto = listaVuelos.getSelectedValue(); 
        String[] partes = texto.split("\\|");
        
        DatosReserva.vueloGlobal = (partes.length > 0) ? partes[0].trim() : texto;
        DatosReserva.precioUnitario = (partes.length > 2) ? partes[2].trim() : "$0.00";
        DatosReserva.rutaGlobal = cboOrigen.getSelectedItem() + " - " + cboDestino.getSelectedItem();

        vistaCabina cabina = new vistaCabina();
        cabina.setVisible(true);
        cabina.setLocationRelativeTo(null);
        this.dispose();
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un vuelo.");
    }
    }//GEN-LAST:event_btnContinuarActionPerformed

